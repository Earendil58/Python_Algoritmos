#Ingreso de datos
pies = float(input('Ingrese una medida en pies: '))

#Asignacion de valores
yardas = pies / 3

pulgadas = pies * 12

centimetros = pulgadas * 2.54

metros = centimetros / 100

print('Los pies ingresados en yardas son: ', yardas)
print('Los pies ingresados en pulgadas son: ', pulgadas )
print('Los pies ingresados en centimetros son: ', centimetros)
print('Los pies ingresados en metros son: ', metros)
